const languageOverrides = { hi, mr, gu };

for (const language of supportedLanguageCodes) {
  const languageDir = path.join(localeRoot, language);
  fs.mkdirSync(languageDir, { recursive: true });
  const overrides = languageOverrides[language] || {};

  for (const namespace of namespaces) {
    const namespaceResource = deepMerge(resources[namespace] || {}, overrides[namespace] || {});
    fs.writeFileSync(
      path.join(languageDir, `${namespace}.json`),
      `${JSON.stringify(namespaceResource, null, 2)}\n`,
      "utf8"
    );
  }
}

console.log(`Seeded ${namespaces.length} namespaces for ${supportedLanguageCodes.length} languages in ${localeRoot}.`);
